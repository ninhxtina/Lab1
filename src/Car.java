public class Car {
    //instance variables
    private String make;
    private String model;
    private int mph;

    //getter & setter methods
    public void setMake (String make) {
        this.make = make;
    }

    public void setModel (String model) {
        this.model = model;
    }

    public void setMiles (int miles) {
        this.mph = mph;
    }

    public String getMake() {
        return this.make;
    }

    public String getModel() {
        return this.model;
    }

    public int getMiles() {
        return this.mph;
    }
}//end car class
